package pojo;

public class Location {
	
	private double lat;
	private double lag;
	
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLag() {
		return lag;
	}
	public void setLag(double lag) {
		this.lag = lag;
	}
	
}
